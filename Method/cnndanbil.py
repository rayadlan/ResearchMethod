# 8. Model CNN

class CNNTextClassifier(nn.Module):
    def __init__(
        self,
        vocab_size,
        embed_dim=64,
        num_filters=100,
        kernel_sizes=(3, 4, 5),
        dropout=0.5
    ):
        super(CNNTextClassifier, self).__init__()

        self.embedding = nn.Embedding(
            vocab_size,
            embed_dim,
            padding_idx=0
        )

        self.convs = nn.ModuleList([
            nn.Conv2d(
                in_channels=1,
                out_channels=num_filters,
                kernel_size=(kernel_size, embed_dim)
            )
            for kernel_size in kernel_sizes
        ])

        self.dropout = nn.Dropout(dropout)
        self.fc = nn.Linear(num_filters * len(kernel_sizes), 1)

    def forward(self, x):
        x = self.embedding(x)
        x = x.unsqueeze(1)

        conv_outputs = [
            torch.relu(conv(x)).squeeze(3)
            for conv in self.convs
        ]

        pooled_outputs = [
            torch.max(conv_output, dim=2)[0]
            for conv_output in conv_outputs
        ]

        x = torch.cat(pooled_outputs, dim=1)
        x = self.dropout(x)
        x = self.fc(x)

        return x.squeeze(1)

# 9. Model BiLSTM

class BiLSTMTextClassifier(nn.Module):
    def __init__(
        self,
        vocab_size,
        embed_dim=64,
        hidden_dim=128,
        num_layers=2,
        dropout=0.5
    ):
        super(BiLSTMTextClassifier, self).__init__()

        self.embedding = nn.Embedding(
            vocab_size,
            embed_dim,
            padding_idx=0
        )

        self.lstm = nn.LSTM(
            input_size=embed_dim,
            hidden_size=hidden_dim,
            num_layers=num_layers,
            batch_first=True,
            bidirectional=True,
            dropout=dropout
        )

        self.dropout = nn.Dropout(dropout)
        self.fc = nn.Linear(hidden_dim * 2, 1)

    def forward(self, x):
        x = self.embedding(x)

        output, (hidden, cell) = self.lstm(x)

        forward_hidden = hidden[-2]
        backward_hidden = hidden[-1]

        x = torch.cat((forward_hidden, backward_hidden), dim=1)
        x = self.dropout(x)
        x = self.fc(x)

        return x.squeeze(1)

# 10. Evaluation Function

def evaluate_model(model, data_loader):
    model.eval()

    y_true = []
    y_pred = []

    with torch.no_grad():
        for X_batch, y_batch in data_loader:
            X_batch = X_batch.to(device)

            outputs = model(X_batch)
            probs = torch.sigmoid(outputs)
            preds = (probs >= 0.5).long().cpu().numpy()

            y_pred.extend(preds)
            y_true.extend(y_batch.numpy().astype(int))

    accuracy = accuracy_score(y_true, y_pred)
    precision = precision_score(y_true, y_pred, zero_division=0)
    recall = recall_score(y_true, y_pred, zero_division=0)
    f1 = f1_score(y_true, y_pred, zero_division=0)
    cm = confusion_matrix(y_true, y_pred)

    return accuracy, precision, recall, f1, cm, y_true, y_pred

# 11. Training Function

def train_model(
    model,
    model_name,
    train_loader,
    val_loader,
    epochs=5,
    learning_rate=0.001
):
    model = model.to(device)

    criterion = nn.BCEWithLogitsLoss()
    optimizer = torch.optim.Adam(model.parameters(), lr=learning_rate)

    history = {
        "train_loss": [],
        "val_accuracy": [],
        "val_precision": [],
        "val_recall": [],
        "val_f1": []
    }

    print(f"\n===== Training {model_name} =====")

    for epoch in range(1, epochs + 1):
        model.train()
        total_loss = 0

        for X_batch, y_batch in train_loader:
            X_batch = X_batch.to(device)
            y_batch = y_batch.to(device)

            optimizer.zero_grad()

            outputs = model(X_batch)
            loss = criterion(outputs, y_batch)

            loss.backward()
            optimizer.step()

            total_loss += loss.item()

        avg_train_loss = total_loss / len(train_loader)

        val_acc, val_prec, val_rec, val_f1, _, _, _ = evaluate_model(
            model,
            val_loader
        )

        history["train_loss"].append(avg_train_loss)
        history["val_accuracy"].append(val_acc)
        history["val_precision"].append(val_prec)
        history["val_recall"].append(val_rec)
        history["val_f1"].append(val_f1)

        print(
            f"Epoch {epoch}/{epochs} | "
            f"Loss: {avg_train_loss:.6f} | "
            f"Val Acc: {val_acc:.6f} | "
            f"Val Precision: {val_prec:.6f} | "
            f"Val Recall: {val_rec:.6f} | "
            f"Val F1: {val_f1:.6f}"
        )

    return model, history


# 12. Plot History

def plot_history(history, model_name):
    epochs_range = range(1, len(history["train_loss"]) + 1)

    plt.figure(figsize=(8, 5))
    plt.plot(epochs_range, history["train_loss"], marker="o")
    plt.title(f"{model_name} Training Loss")
    plt.xlabel("Epoch")
    plt.ylabel("Loss")
    plt.grid(True)
    plt.show()

    plt.figure(figsize=(8, 5))
    plt.plot(epochs_range, history["val_accuracy"], marker="o", label="Validation Accuracy")
    plt.plot(epochs_range, history["val_f1"], marker="o", label="Validation F1-score")
    plt.title(f"{model_name} Validation Accuracy and F1-score")
    plt.xlabel("Epoch")
    plt.ylabel("Score")
    plt.legend()
    plt.grid(True)
    plt.show()

# 13. Plot Confusion Matrix

def plot_confusion_matrix(cm, model_name):
    plt.figure(figsize=(6, 5))
    plt.imshow(cm)
    plt.title(f"Confusion Matrix - {model_name}")
    plt.xlabel("Predicted Label")
    plt.ylabel("Actual Label")

    class_names = ["True", "Fake"]
    plt.xticks([0, 1], class_names)
    plt.yticks([0, 1], class_names)

    for i in range(cm.shape[0]):
        for j in range(cm.shape[1]):
            plt.text(
                j,
                i,
                cm[i, j],
                ha="center",
                va="center"
            )

    plt.colorbar()
    plt.show()

