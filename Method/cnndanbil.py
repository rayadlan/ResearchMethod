# 4. Cleaning Text
def clean_text(text):
    text = text.lower()
    text = re.sub(r"http\S+|www\S+", " ", text)
    text = re.sub(r"[^a-zA-Z0-9\s]", " ", text)
    text = re.sub(r"\s+", " ", text).strip()
    return text

cleaned_texts = [clean_text(text) for text in texts]
tokenized_texts = [text.split() for text in cleaned_texts]

# 5. Build Vocabulary
MAX_VOCAB_SIZE = 20000
MAX_SEQ_LEN = 200

word_counter = Counter()

for tokens in tokenized_texts:
    word_counter.update(tokens)

most_common_words = word_counter.most_common(MAX_VOCAB_SIZE - 2)

word2idx = {
    "<PAD>": 0,
    "<OOV>": 1
}

for idx, (word, count) in enumerate(most_common_words, start=2):
    word2idx[word] = idx

vocab_size = len(word2idx)
print("Vocabulary size:", vocab_size)

def tokens_to_sequence(tokens, word2idx, max_len):
    sequence = [word2idx.get(token, word2idx["<OOV>"]) for token in tokens]

    if len(sequence) < max_len:
        sequence = sequence + [word2idx["<PAD>"]] * (max_len - len(sequence))
    else:
        sequence = sequence[:max_len]

    return sequence

sequences = [
    tokens_to_sequence(tokens, word2idx, MAX_SEQ_LEN)
    for tokens in tokenized_texts
]

